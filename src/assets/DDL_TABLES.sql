CREATE TABLE AMTDB.TRACKING_DESC(
  TRACKING_ID VARCHAR(50) NOT NULL,
  ITEM_CURRENT_LOCATION   VARCHAR(50)
);

CREATE TABLE AMTDB.SHIPPING(
  SHIPPING_ID   VARCHAR(25),
  SHIPMENT_METHOD VARCHAR(25),
